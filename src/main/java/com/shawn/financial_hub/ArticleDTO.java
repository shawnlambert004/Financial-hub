package com.shawn.financial_hub;

public class ArticleDTO {
    String title;
    String imageURL;
    String byLine;
    String username;
    String content;
    String sources;

    public String getImageURL() {
        return imageURL;
    }

    public String getTitle() {
        return title;
    }

    public String getByLine() {
        return byLine;
    }

    public String getUsername() {
        return username;
    }

    public String getContent() {
        return content;
    }

    public String getSources() {
        return sources;
    }
}
